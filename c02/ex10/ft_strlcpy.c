unsigned int	ft_strlcpy(char *dest, char *src, unsigned int size)
{
	int unsigned	len;
	int unsigned	i;

	len = 0;
	while (src[len])
	{
		len++;
	}
	if (size > 0)
	{
		i = 0;
		while (src[i] && i < size - 1)
		{
			dest[i] = src[i];
			i++;
		}
		dest[i] = '\0';
	}
	return (len);
}
